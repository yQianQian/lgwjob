package com.kgc.pojo;

public class Vocation {
    private Integer id;

    private Integer vparentid;

    private Integer vocationname;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getVparentid() {
        return vparentid;
    }

    public void setVparentid(Integer vparentid) {
        this.vparentid = vparentid;
    }

    public Integer getVocationname() {
        return vocationname;
    }

    public void setVocationname(Integer vocationname) {
        this.vocationname = vocationname;
    }
}