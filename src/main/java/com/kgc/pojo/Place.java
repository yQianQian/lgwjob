package com.kgc.pojo;

public class Place {
    private Integer id;

    private Integer pparentid;

    private Integer placename;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getPparentid() {
        return pparentid;
    }

    public void setPparentid(Integer pparentid) {
        this.pparentid = pparentid;
    }

    public Integer getPlacename() {
        return placename;
    }

    public void setPlacename(Integer placename) {
        this.placename = placename;
    }
}